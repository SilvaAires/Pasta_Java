package Modulo_01;

public class Metodos {
	public static int somar(int n, int n2) {
		return n + n2;
	}
	                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                
	public static void imprimir(int n) {
		System.out.println(n);
	}
	
	public static void main(String[] args) {
		imprimir(somar(13, 5));
	}
}
